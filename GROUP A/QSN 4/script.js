// Arithmetic Operators
let a = 10;
let b = 5;

console.log("Addition: " + (a + b));        // Output: 15
console.log("Subtraction: " + (a - b));     // Output: 5
console.log("Multiplication: " + (a * b));  // Output: 50
console.log("Division: " + (a / b));        // Output: 2
console.log("Modulus: " + (a % b));         // Output: 0
console.log("Exponentiation: " + (a ** b)); // Output: 100000

console.log("\n"); // New line for separation

// Logical Operators
console.log("Logical Operators:");
let x = true;
let y = false;

console.log("AND (x && y): " + (x && y)); // Output: false
console.log("OR (x || y): " + (x || y));  // Output: true
console.log("NOT (!x): " + (!x));          // Output: false

console.log("\n"); // New line for separation

// Comparison Operators
console.log("Comparison Operators:");
let p = 20;
let q = 15;

console.log("Equal (p == q): " + (p == q));       // Output: false
console.log("Not Equal (p != q): " + (p != q));   // Output: true
console.log("Greater Than (p > q): " + (p > q));  // Output: true
console.log("Less Than (p < q): " + (p < q));     // Output: false
console.log("Greater Than or Equal (p >= q): " + (p >= q)); // Output: true
console.log("Less Than or Equal (p <= q): " + (p <= q));    // Output: false