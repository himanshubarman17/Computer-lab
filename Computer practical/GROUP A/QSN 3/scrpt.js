// String
let n = "John wick";
console.log("String:", n);

// Number
let age = 25;
console.log("Number:", age);

// Boolean
let isStudent = true;
console.log("Boolean:", isStudent);

// Null
let emptyValue = null;
console.log("Null:", emptyValue);

// Undefined
let unassignedValue;
console.log("Undefined:", unassignedValue);

// Object
let person = {
  firstName: "John",
  lastName: "wick",
  age: 28
};
console.log("Object:", person);

// Array
let fruits = ["Apple", "Banana", "Cherry"];
console.log("Array:", fruits);

// Symbol
let uniqueId = Symbol("id");
console.log("Symbol:", uniqueId);

// BigInt (used for very large integers)
let largeNumber = BigInt(9007199254740991);
console.log("BigInt:", largeNumber);

// Function
function greet() {
  return "Hello World!";
}
console.log("Function:", greet());


